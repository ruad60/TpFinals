    public void createNewFunction(){
        if(!file.exists()){
            try {
                file.createNewFile();
            } catch (IOException ex) {
                Logger.getLogger(NinongsResto.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
    
    int lineCount = 0;
    public void countLine(){
        try {
            RandomAccessFile raf = new RandomAccessFile(file,"rw");
            while(raf.readLine() != null){
                lineCount++;
            }
            raf.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(NinongsResto.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(NinongsResto.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void showMenu(){
        String titleLine = "Item,ID,Price";
        try {
            BufferedReader reader = new BufferedReader(new FileReader(file));
            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
            model.getDataVector().removeAllElements();
            model.fireTableDataChanged();
            String[] splitTitle = titleLine.trim().split(",");
            model.setColumnIdentifiers(splitTitle);
            
            Object[] object = reader.lines().toArray();
            
            for (int i = 0; i<object.length; i++){
                String[] dataSplit = object[i].toString().trim().split(",");
                model.addRow(dataSplit);
            }
            reader.close();
            
        } catch (FileNotFoundException ex) {
            Logger.getLogger(NinongsResto.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(NinongsResto.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void addMenu(){
        countLine();
        try {
            RandomAccessFile raf = new RandomAccessFile(file,"rw");
            for (int i = 1; i<=lineCount; i++){
               raf.readLine();
            }
            raf.writeBytes(txtItem.getText()+","+txtID.getText()+","+txtPrice.getText());
            raf.writeBytes(System.lineSeparator());
            raf.close();
            txtItem.setText("");
            txtID.setText("");
            txtPrice.setText("");
        } catch (FileNotFoundException ex) {
            Logger.getLogger(NinongsResto.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(NinongsResto.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
